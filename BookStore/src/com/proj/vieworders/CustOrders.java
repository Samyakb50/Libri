package com.proj.vieworders;

public class CustOrders {

	private int oid;
	private int sum;
	private String status;
	private String mopay;
	private String date;
	public int getOid() {
		return oid;
	}
	public void setOid(int oid) {
		this.oid = oid;
	}
	public int getSum() {
		return sum;
	}
	public void setSum(int sum) {
		this.sum = sum;
	}
	public String getStatus() {
		return status;
	}
	public void setStatus(String status) {
		this.status = status;
	}
	public String getMopay() {
		return mopay;
	}
	public void setMopay(String mopay) {
		this.mopay = mopay;
	}
	public String getDate() {
		return date;
	}
	public void setDate(String date) {
		this.date = date;
	}
	
}
